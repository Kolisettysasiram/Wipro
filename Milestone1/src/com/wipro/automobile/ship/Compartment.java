package com.wipro.automobile.ship;

public class Compartment {
      
	int height,width,breadth;
	Compartment(int h,int w,int b)
       {
    	   height=h;
    	   breadth=b;
    	   width =w;
       }
	public int getHeight() {
		return height;
	}
	public int getWidth() {
		return width;
	}
	public int getBreadth() {
		return breadth;
	}
	
	
}
